var add = function(num1, num2) {
    return num1 + num2;
  };
  
  var subtract = function(num1, num2) {
    return num1 - num2;
  };
  
  var multiply = function(num1, num2) {
    return num1 * num2;
  };
  
  var divide = function(num1, num2) {
    return num1 / num2; //ရလာဒ်
  };
  
  var secdivide = function(num1, num2) {
    return num1 % num2; //အကြွင်း
  };
  